void main() {
  print(40 + 20); // >> 60 | Adição
  print('40' + '2'); // >> 402 | Concatenação
  print(65 - 8); // >> 57 | Subtração
  print(-(-49)); // >> 49 | Negação
  print(6 * 7); // >> 42 | Multiplicação
  print(21 / 5); // >> 4.2 | Divisão
  print(21 ~/ 5); // >> 4 | Divisão com retorno da parte inteira
  print(11 % 2); // 1 | Resto da divisão
}